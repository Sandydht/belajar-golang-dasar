package main

import "fmt"

func main() {
	// Kode Program Operasi Matematika
	var a = 10
	var b = 10
	var c = a + b
	fmt.Println(c)

	// Kode Program Augmented Assignments
	var i = 10
	i += 10
	fmt.Println(i)

	// Kode Program Unary Operator
	i++
	fmt.Println(i)

	var negative = -100
	var positive = +100 // by default sudah positif
	fmt.Println(negative)
	fmt.Println(positive)
}
